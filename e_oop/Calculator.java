package e_oop;

public class Calculator {

	// +, -, *, /, % 연산이 가능한 5개의 메서드 생성

	long add(long a, long b) {
		return a + b;
	}

	long subtract(long a, long b) {
		return a - b;
	}

	long multiply(long a, long b) {
		return a * b;
	}

	long divide(long a, long b) {
		return a / b;
	}

	long mod(long a, long b) {
		return a % b;
	}
}
